// State Variables
let currentLang = "zh";
let mockActiveTab = "folders";
let mockPosition = "Right";
let mockTheme = "Dark";

// Initialize elements
document.addEventListener("DOMContentLoaded", () => {
    // 1. Language Button Toggle
    const langBtn = document.getElementById("lang-btn");
    langBtn.addEventListener("click", () => {
        const nextLang = currentLang === "zh" ? "en" : "zh";
        switchLanguage(nextLang);
    });

    // 2. Interactive Mockup - Edge Hover / Workspace hover
    const mockWorkspace = document.getElementById("mock-workspace");
    const mockTriggerEdge = document.getElementById("mock-trigger-edge");
    const mockSidebar = document.getElementById("mock-sidebar");
    
    mockTriggerEdge.addEventListener("mouseenter", () => {
        mockSidebar.classList.remove("collapsed");
    });
    
    mockWorkspace.addEventListener("mouseleave", () => {
        mockSidebar.classList.add("collapsed");
        closeSettingsDialog();
    });

    // 3. Tab Buttons
    const tabBtns = document.querySelectorAll(".tab-btn");
    tabBtns.forEach(btn => {
        btn.addEventListener("click", (e) => {
            tabBtns.forEach(b => b.classList.remove("active"));
            btn.classList.add("active");
            mockActiveTab = btn.getAttribute("data-tab");
            
            // Update Header Title based on Active Tab
            const headerTitle = document.getElementById("mock-header-title");
            headerTitle.innerText = btn.innerText;
            
            renderMockList();
        });
    });

    // 4. Mock Settings Toggle
    const mockSettingsBtn = document.getElementById("mock-settings-btn");
    const settingsDialog = document.getElementById("mock-settings-dialog");
    const settingsCloseBtn = document.getElementById("settings-close-btn");
    const settingsCancelBtn = document.getElementById("settings-cancel-btn");
    const settingsSaveBtn = document.getElementById("settings-save-btn");

    mockSettingsBtn.addEventListener("click", () => {
        // Load settings values into select dropdowns
        document.getElementById("settings-theme-select").value = mockTheme;
        document.getElementById("settings-pos-select").value = mockPosition;
        document.getElementById("settings-lang-select").value = currentLang;

        settingsDialog.style.display = "flex";
    });

    const closeSettingsDialog = () => {
        settingsDialog.style.display = "none";
    };

    settingsCloseBtn.addEventListener("click", closeSettingsDialog);
    settingsCancelBtn.addEventListener("click", closeSettingsDialog);
    
    settingsSaveBtn.addEventListener("click", () => {
        // Save values
        const selectedTheme = document.getElementById("settings-theme-select").value;
        const selectedPos = document.getElementById("settings-pos-select").value;
        const selectedLang = document.getElementById("settings-lang-select").value;

        // Apply theme variables classes to mock sidebar
        mockTheme = selectedTheme;
        applyMockTheme(selectedTheme);

        // Apply position to mock trigger and sidebar
        mockPosition = selectedPos;
        applyMockPosition(selectedPos);

        // Apply language
        if (selectedLang !== currentLang) {
            switchLanguage(selectedLang);
        }

        closeSettingsDialog();
    });

    // 5. Initial Render
    switchLanguage("zh");
});

// Switch Page Language
function switchLanguage(lang) {
    currentLang = lang;
    
    // Update HTML lang attribute
    document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";

    // Toggle button text (indicates the next switch target language)
    const langBtn = document.getElementById("lang-btn");
    langBtn.innerText = lang === "zh" ? "English" : "简体中文";

    // Loop through all translation strings
    const dict = translations[lang];
    for (const key in dict) {
        const el = document.getElementById(key);
        if (el) {
            // Check if select option or normal element
            if (el.tagName === "INPUT") {
                el.placeholder = dict[key];
            } else {
                el.innerHTML = dict[key];
            }
        }
    }

    // Translate mockup search box placeholder
    document.getElementById("mock-search-box").placeholder = dict["search-placeholder"];

    // Update active tab buttons label in UI
    document.getElementById("tab-folders-btn").innerText = dict["tab-folders-btn"];
    document.getElementById("tab-files-btn").innerText = dict["tab-files-btn"];
    document.getElementById("tab-recent-btn").innerText = dict["tab-recent-btn"];
    document.getElementById("tab-custom-btn").innerText = dict["tab-custom-btn"];
    
    // Update sort buttons label
    document.getElementById("sort-name-btn").innerText = dict["sort-name-btn"];
    document.getElementById("sort-time-btn").innerText = dict["sort-time-btn"];
    document.getElementById("sort-freq-btn").innerText = dict["sort-freq-btn"];

    // Render mockup list with translated data
    renderMockList();
}

// Render Mock Sidebar items
function renderMockList() {
    const listContainer = document.getElementById("mock-list-container");
    listContainer.innerHTML = "";

    const items = mockData[currentLang][mockActiveTab];
    if (!items) return;

    items.forEach(item => {
        const card = document.createElement("div");
        card.className = "mock-item-card";

        // Icon based on type
        let icon = "📄";
        if (mockActiveTab === "folders") icon = "📁";
        else if (mockActiveTab === "recent") icon = item.isDirectory ? "📁" : "📄";
        else if (mockActiveTab === "custom") icon = item.isDirectory ? "📁" : "📄";

        // Metadata section
        let metaHtml = "";
        if (mockActiveTab === "folders" || mockActiveTab === "files" || mockActiveTab === "custom") {
            metaHtml = `
                <span>${item.time}</span>
                <span>•</span>
                <span class="meta-freq">🔥 ${item.freq}</span>
            `;
        } else if (mockActiveTab === "recent") {
            metaHtml = `<span>${item.time}</span>`;
        }

        // Pinned state pin element
        let pinClass = item.isPinned ? "pin-btn pinned" : "pin-btn";
        // Pinned icon style (push pin character)
        let pinChar = "📌";
        if (mockActiveTab === "custom") {
            pinChar = "🗑️";
            pinClass = "pin-btn";
        }

        card.innerHTML = `
            <span class="card-icon">${icon}</span>
            <div class="card-details">
                <span class="card-name" title="${item.name}">${item.name}</span>
                <span class="card-path" title="${item.path}">${item.path}</span>
                <div class="card-meta">${metaHtml}</div>
            </div>
            <button class="${pinClass}">${pinChar}</button>
        `;

        // Mock open folder on click
        card.addEventListener("click", (e) => {
            // Prevent trigger on pin/delete button
            if (e.target.tagName === "BUTTON") {
                if (mockActiveTab === "custom") {
                    // Sim delete
                    const idx = mockData[currentLang].custom.indexOf(item);
                    if (idx > -1) {
                        mockData["zh"].custom.splice(idx, 1);
                        mockData["en"].custom.splice(idx, 1);
                        renderMockList();
                    }
                } else {
                    // Sim toggle pin
                    item.isPinned = !item.isPinned;
                    renderMockList();
                }
                return;
            }
            alert(currentLang === "zh" ? `打开项: ${item.path}` : `Opening item: ${item.path}`);
        });

        listContainer.appendChild(card);
    });

    // Update Mock Footer status text
    const statusText = document.getElementById("mock-status-text");
    const dict = translations[currentLang];
    
    if (mockActiveTab === "folders") {
        statusText.innerText = dict["status-folders"].replace("{0}", items.length);
    } else if (mockActiveTab === "files") {
        statusText.innerText = dict["status-files"].replace("{0}", items.length);
    } else if (mockActiveTab === "recent") {
        statusText.innerText = dict["status-recent"].replace("{0}", items.length);
    } else if (mockActiveTab === "custom") {
        statusText.innerText = dict["status-custom"].replace("{0}", items.length);
    }
}

// Apply Selected theme to Mockup
function applyMockTheme(theme) {
    const mockSidebar = document.getElementById("mock-sidebar");
    if (theme === "Light") {
        mockSidebar.classList.remove("mock-dark-vars");
        mockSidebar.classList.add("mock-light-vars");
    } else {
        mockSidebar.classList.remove("mock-light-vars");
        mockSidebar.classList.add("mock-dark-vars");
    }
}

// Apply selected Docking position to Mockup
function applyMockPosition(position) {
    const mockTriggerEdge = document.getElementById("mock-trigger-edge");
    const mockSidebar = document.getElementById("mock-sidebar");
    const mockBgText = document.getElementById("mock-bg-sub");
    
    if (position === "Left") {
        // Trigger Edge to Left
        mockTriggerEdge.style.right = "auto";
        mockTriggerEdge.style.left = "0";
        mockTriggerEdge.style.borderRight = "1px dashed var(--accent)";
        mockTriggerEdge.style.borderLeft = "none";

        // Sidebar to Left
        mockSidebar.style.right = "auto";
        mockSidebar.style.left = "0";
        mockSidebar.style.borderRight = "1px solid var(--mock-border)";
        mockSidebar.style.borderLeft = "none";
        
        // Reset collapsed animation transform direction
        mockSidebar.style.transform = "";
        if (mockSidebar.classList.contains("collapsed")) {
            mockSidebar.style.transform = "translateX(-235px)";
        }

        mockBgText.innerText = currentLang === "zh" ? "请将鼠标滑向最左侧边缘" : "Move your cursor to the leftmost edge";
    } else {
        // Trigger Edge to Right
        mockTriggerEdge.style.left = "auto";
        mockTriggerEdge.style.right = "0";
        mockTriggerEdge.style.borderLeft = "1px dashed var(--accent)";
        mockTriggerEdge.style.borderRight = "none";

        // Sidebar to Right
        mockSidebar.style.left = "auto";
        mockSidebar.style.right = "0";
        mockSidebar.style.borderLeft = "1px solid var(--mock-border)";
        mockSidebar.style.borderRight = "none";
        
        mockSidebar.style.transform = "";
        if (mockSidebar.classList.contains("collapsed")) {
            mockSidebar.style.transform = "translateX(235px)";
        }
        
        mockBgText.innerText = currentLang === "zh" ? "请将鼠标滑向最右侧边缘" : "Move your cursor to the rightmost edge";
    }
}
